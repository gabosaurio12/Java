class TiposDeDatos
{
	public static void main(String args[])
	{
		char c = 'C';
		int i = 1924;
		long l = 16516153;
		float f = 4.1452f;
		double d = 4.65465136;
		byte b = 4;
		short s = 56;
		String sSNO = "Que la fuerza te acompañe";
		String sCNO = "Que la fuerza te acompañe siempre";

		System.out.println("Char: " + c);
		System.out.println("Integer: "  + i);
		System.out.println("Long: " + l);
		System.out.println("Float: " + f);
		System.out.println("Double: "+ d);
		System.out.println("Byte: " + b);
		System.out.println("Short: " + s);
		System.out.println("String sin nuevo operador");
		System.out.println("String:" + sSNO);
		System.out.println("String con nuevo operador");
		System.out.println("String:" + sCNO);
	}
}
